// Dart program to illustrate data types

void main() {
  // int data type
  int age = 23;
  print('Age: $age');

  // double data type
  double height = 5.8;
  print('Height: $height feet');

  // String data type
  String name = 'Momanyi Brian';
  print('Name: $name');

  // List data type
  List hobby = ["Physical Fitness", "Skatting"];
  print('Hobbies: $hobby');

  // Map data type including a boolean
  Map person = {'cohort': 3, 'learning': 'Dart', 'isMarried': false};
  print('Person: $person');
}
